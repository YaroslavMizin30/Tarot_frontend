import type { Spread } from '../types';
/**
 * Готовит промпт для анализа всех раскладов из списка
 * @param spreads Список раскладов для анализа
 * @returns Подготовленный промпт для анализа
 */
export const prepareSpreadsAnalysisPrompt = (spreads: Spread[]): string => {
  const promptSections = [
    'Анализируйте следующие расклады Таро и предоставьте комплексный взгляд на ситуацию:',
    '',
    `Всего раскладов: ${spreads.length}`,
    '',
  ];

  spreads.forEach((spread, index) => {
    promptSections.push(
      `РАСКЛАД #${index + 1}:`,
      `Название: ${spread.title || 'Без названия'}`,
      `Дата: ${spread.date}`,
      `Вопрос: ${spread.question}`,
      `Интерпретация: ${spread.interpretation}`,
      `Рейтинг: ${spread.rating || 'Не оценено'}`,
      '',
    );
  });

  promptSections.push(
    'Пожалуйста, проанализируйте:',
    '1. Общую тенденцию и развитие ситуации',
    '2. Повторяющиеся темы и паттерны',
    '3. Изменения во времени (если есть несколько дат)',
    '4. Эмоциональный фон и ключевые сообщения',
    '5. Рекомендации на основе всех раскладов',
    '6. Возможные сценарии развития событий',
    '',
    'Сформируйте ответ в структурированном виде с четкими выводами.',
  );

  return promptSections.join('\n');
};
